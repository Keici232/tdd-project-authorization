public class Member {

    String name;
    String passwrod;


    public Member(String name, String passwrod) {
        this.name = name;
        this.passwrod = passwrod;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPasswrod() {
        return passwrod;
    }

    public void setPasswrod(String passwrod) {
        this.passwrod = passwrod;
    }
}
